# Tipo de dado númerico do tipo int
velocidade_internet = 10
print(velocidade_internet)

# Tipo de dado númerico do tipo float
nota_aluno = 9.5
print(nota_aluno)

# Tipo de dado boleano
esta_aberto = True
print(esta_aberto)

# Tipo de dado string
slogan = "Feito é melhor do que perfeito!"
print(slogan)

# Unpacking
a, b, c, d = 1, 2, 3, 4
print(a)
print(b)
print(c)
print(d)

# Descobrir o tipo de dado
print(type(nota_aluno)) 