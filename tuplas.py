# Tuplas
# Tuplas é uma forma de armazenar dados igual uma lista, mas com a restrição de não poder alterar ou reatribuir os valores 

sites = ('youtube.com', 'facebook.com', 'instagram.com')
numeros = (76, 35, 19)

# Juntar as tuplas em uma só
dados_do_sistema = sites + numeros
print(dados_do_sistema)

# Acessar os valores da tupla é igual a lista
print(dados_do_sistema[5])
print(sites)
print(numeros)